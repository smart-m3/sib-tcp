int sRunning = 0;

void SignalStop(int a)
{
	if (sRunning)
	{
		HIPDEX_Stop();
		sRunning = 0;
	}
};

int main()
{
	printf("** HIP DEX Responder V0.2\n");
	printf("** Press Ctrl-C to quit...\n");

	if (getuid() != 0)
	{
		printf("** Program must be run as root.\n");
		return 1;
	}

	HIPDEX_Start();
	
	sRunning = 1;

	signal(SIGINT, SignalStop);
	
	while (sRunning)
	{
		struct sockaddr_in iAddr;
		inet_pton(AF_INET, "193.167.187.74", &iAddr.sin_addr);
		void *sa = HIPDEX_GetSA(iAddr.sin_addr.s_addr);
		sleep(2);
		HIPDEX_RemoveSA(iAddr.sin_addr.s_addr);
		sa = HIPDEX_GetSA(iAddr.sin_addr.s_addr);
	}

	return 0;
};
